my_string=input('Введите любой текст:')
print(my_string.upper())    # Выводим строку в верхнем регистре
print(my_string.lower())    # Выводим строку в нижнем регистре
print(my_string.replace(' ', '')) # Удаление всех пробелов
print(my_string[0])     # Вывожу первый символ строки my_string
print(my_string[-1])    # Вывожу последний символ строки my_string
print('Длина строки:', len(my_string))
